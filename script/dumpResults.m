function dumpResults(test_params, res)
    fd = fopen("dump.txt", 'w+');
    if test_params.dimension == 3
        formatString = fileread("dumpFormat3.txt");
        fprintf(fd, formatString, ...
            test_params.dimension, ...
            test_params.sampling_step, ...
            test_params.sample_amount, ...
            test_params.minimum(1), ...
            test_params.minimum(2), ...
            test_params.minimum(3), ...
            res.minimum(1), ...
            res.minimum(2), ...
            res.minimum(3), ...
            test_params.start_point(1), ...
            test_params.start_point(2), ...
            test_params.start_point(3), ...
            res.f_objective, ...
            test_params.length, ...
            res.halvings, ... 
            test_params.minLength, ...
            res.length, ...
            test_params.max_flips, ...
            res.flips, ...
            test_params.tolerance, ...
            res.perc_error ...
        );
    elseif test_params.dimension == 2
        formatString = fileread("dumpFormat2.txt");
        fprintf(fd, formatString, ...
            test_params.dimension, ...
            test_params.sampling_step, ...
            test_params.sample_amount, ...
            test_params.minimum(1), ...
            test_params.minimum(2), ...
            res.minimum(1), ...
            res.minimum(2), ...
            test_params.start_point(1), ...
            test_params.start_point(2), ...
            res.f_objective, ...
            test_params.length, ...
            res.halvings, ...
            test_params.minLength, ...
            res.length, ...
            test_params.max_flips, ...
            res.flips, ...
            test_params.tolerance, ...
            res.perc_error ...
        );
    end
    fclose(fd);
end